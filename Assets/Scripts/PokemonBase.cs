    public static int GetBaseLevelExp(int level, ExperienceGroup expGroup)
    {
        switch (expGroup)
        {
            case ExperienceGroup.MediumFast:
                return mediumFast[level - 1];

            case ExperienceGroup.Erratic:
                return erractic[level - 1];

            case ExperienceGroup.Fluctuating:
                return fluctuating[level - 1];

            case ExperienceGroup.MediumSlow:
                return mediumSlow[level - 1];

            case ExperienceGroup.Fast:
                return fast[level - 1];

            case ExperienceGroup.Slow:
                return slow[level - 1];
            default:
                return -1;
        }
    }

    public static int GetNextLevelExpRequirement(int level, ExperienceGroup expGroup)
    {
        switch (expGroup)
        {
            case ExperienceGroup.MediumFast:
                return expToNextLevel_mediumFast[level - 1];

            case ExperienceGroup.Erratic:
                return expToNextLevel_erractic[level - 1];

            case ExperienceGroup.Fluctuating:
                return expToNextLevel_fluctuating[level - 1];

            case ExperienceGroup.MediumSlow:
                return expToNextLevel_mediumSlow[level - 1];

            case ExperienceGroup.Fast:
                return expToNextLevel_fast[level - 1];

            case ExperienceGroup.Slow:
                return expToNextLevel_slow[level - 1];
            default:
                return -1;
        }
    }

